// The modulus operator (%) returns the remainder of a division problem

// For example, 6 divided by 3 (6/3) = 2 with no remainder
6 / 3
// So 6 modulus 3 (6%3) = 0
6 % 3
// Now let's try 7 / 3 - notice that the response is a float value
// This indicates that there should be a remainder
// This comes back as 2 with a remainder of 1
7 / 3
// So 7 modulus 3 (7%3) = 1
7 % 3

// What can we do with modulus?

// * See if a number is even or odd
// Any number evenly divisible by 2 is "even".

